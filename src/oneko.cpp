void Oneko::init()
{
    if (!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS))
    {
        Serial.println(F("Display Initialization Error"));
        for (;;)
            ; // Don't proceed, loop forever
    }

    // Show initial display buffer contents on the screen
    display.display();
    delay(2000);
    display.clearDisplay();
}

void Oneko::face(int face)
{
    display.clearDisplay();
    display.drawBitmap(32, 0, faces[face - 1], 64, 64, 1);
    display.display();
}

void Oneko::scratch_ears(int frame_duration)
{
    static long current_millis, pre_millis = 0;
    static bool frame1 = true;

    current_millis = millis();

    if (current_millis - pre_millis >= frame_duration)
    {
        if (frame1)
        {
            face(19);
        }
        else
        {
            face(20);
        }
        frame1 = !frame1;
        pre_millis = millis();
    }
}

void Oneko::sleep(int frame_duration)
{
    static long current_millis, pre_millis = 0;
    static bool frame1 = true;

    current_millis = millis();

    if (current_millis - pre_millis >= frame_duration)
    {
        if (frame1)
        {
            face(21);
        }
        else
        {
            face(22);
        }
        frame1 = !frame1;
        pre_millis = millis();
    }
}

void Oneko::go_to_sleep(int yawn_duration, int scratch_ears_duration)
{
    static long current_millis, pre_millis = millis();
    static bool is_yawn = true, is_scratch_ears = false;

    current_millis = millis();

    if (is_yawn)
    {
        face(18);
        if (current_millis - pre_millis >= yawn_duration)
        {
            is_yawn = false;
            is_scratch_ears = true;
        }
    }
    else if (is_scratch_ears)
    {
        scratch_ears();
        if (current_millis - pre_millis >= scratch_ears_duration)
        {
            is_scratch_ears = false;
        }
    }
    else
    {
        sleep();
    }
}