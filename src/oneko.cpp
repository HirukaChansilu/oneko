void Oneko::init()
{
    if (!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS))
    {
        Serial.println(F("Display Initialization Error"));
        for (;;)
            ;
    }
    else
    {
        Serial.println("Display Initialized correctly!");
    }

    display.display();
    delay(2000);
    display.clearDisplay();

    display.setTextSize(1);
    display.setTextColor(WHITE);
}

bool Oneko::continue_with_error(int warning_duration)
{
    static bool show_error = true, frame1 = true;
    static long current_millis, pre_millis = millis();

    if (show_error)
    {
        current_millis = millis();

        if (current_millis - pre_millis >= warning_duration / 2)
        {
            if (frame1)
            {
                frame1 = false;
            }
            else
            {
                show_error = false;
            }
            pre_millis = millis();
        }

        display.clearDisplay();
        display.drawBitmap(39, 0, warning, 50, 50, 1);

        if (frame1)
        {
            display.setCursor(5, 52);
            display.print("Accelerometer Error!");
        }
        else
        {
            display.setCursor(32, 52);
            display.print("Proceeding");
        }

        display.display();
    }

    return !show_error;
}

void Oneko::animate(int frame1, int frame2, int frame_duration)
{
    static long current_millis, pre_millis = millis();
    static bool show_frame1 = true;

    current_millis = millis();

    if (current_millis - pre_millis >= frame_duration)
    {
        if (show_frame1)
        {
            face(frame1);
        }
        else
        {
            face(frame2);
        }
        show_frame1 = !show_frame1;
        pre_millis = millis();
    }
}

void Oneko::face(int face)
{
    display.clearDisplay();
    display.drawBitmap(32, 0, faces[face - 1], 64, 64, 1);
    display.display();
}

void Oneko::scratch_ears(int frame_duration)
{
    animate(19, 20, frame_duration);
}

void Oneko::sleep(int frame_duration)
{
    animate(21, 22, frame_duration);
}

void Oneko::go_to_sleep(int yawn_duration, int scratch_ears_duration)
{
    static long current_millis, pre_millis = millis();
    static bool is_yawn = true, is_scratch_ears = false;

    current_millis = millis();

    if (is_yawn)
    {
        face(18);
        if (current_millis - pre_millis >= yawn_duration)
        {
            is_yawn = false;
            is_scratch_ears = true;
        }
    }
    else if (is_scratch_ears)
    {
        scratch_ears();
        if (current_millis - pre_millis >= scratch_ears_duration)
        {
            is_scratch_ears = false;
        }
    }
    else
    {
        sleep();
    }
}

